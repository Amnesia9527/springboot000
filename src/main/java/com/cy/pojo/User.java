package com.cy.pojo;


public class User {

  private String name;
  private String pass;
  private long id;


  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }


  public String getPass() {
    return pass;
  }

  public void setPass(String pass) {
    this.pass = pass;
  }


  public long getId() {
    return id;
  }

  public void setId(long id) {
    this.id = id;
  }

}
